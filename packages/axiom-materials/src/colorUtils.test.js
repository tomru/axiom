import { rgb2hex, hex2rgb } from './colorUtils';

test.each`
  r      | g      | b      | expected
  ${0}   | ${0}   | ${0}   | ${'#000000'}
  ${255} | ${0}   | ${0}   | ${'#ff0000'}
  ${0}   | ${255} | ${0}   | ${'#00ff00'}
  ${0}   | ${0}   | ${255} | ${'#0000ff'}
  ${0}   | ${255} | ${255} | ${'#00ffff'}
  ${255} | ${255} | ${0}   | ${'#ffff00'}
  ${255} | ${0} | ${255}   | ${'#ff00ff'}
  ${255} | ${255} | ${255} | ${'#ffffff'}
  ${63}  | ${63}  | ${63}  | ${'#3f3f3f'}
  ${183} | ${71}  | ${146} | ${'#b74792'}
  ${35}  | ${158} | ${219} | ${'#239edb'}
`('rgb2hex returns $expected for { r: $r, g: $g, b: $b}', ({ r, g, b, expected }) => {
  expect(rgb2hex({ r, g, b })).toBe(expected);
});

test.each`
 input        | expected
 ${'#000000'} | ${{ r: 0, g: 0, b: 0 }}
 ${'#ff0000'} | ${{ r: 255, g: 0, b: 0 }}
 ${'#00ff00'} | ${{ r: 0, g: 255, b: 0 }}
 ${'#0000ff'} | ${{ r: 0, g: 0, b: 255 }}
 ${'#00ffff'} | ${{ r: 0, g: 255, b: 255 }}
 ${'#ffff00'} | ${{ r: 255, g: 255, b: 0 }}
 ${'#ff00ff'} | ${{ r: 255, g: 0, b: 255 }}
 ${'#ffffff'} | ${{ r: 255, g: 255, b: 255 }}
 ${'#3f3f3f'} | ${{ r: 63, g: 63, b: 63 }}
 ${'#b74792'} | ${{ r: 183, g: 71, b: 146 }}
 ${'#239edb'} | ${{ r: 35, g: 158, b: 219 }}
 ${'#000'}    | ${{ r: 0, g: 0, b: 0 }}
 ${'#f00'}    | ${{ r: 255, g: 0, b: 0 }}
 ${'#0f0'}    | ${{ r: 0, g: 255, b: 0 }}
 ${'#ff0'}    | ${{ r: 255, g: 255, b: 0 }}
 ${'#0ff'}    | ${{ r: 0, g: 255, b: 255 }}
 ${'#fff'}    | ${{ r: 255, g: 255, b: 255 }}
 ${'#e39'}    | ${{ r: 238, g: 51, b: 153 }}
 ${'bogus'}   | ${null}
 ${''}        | ${null}
`('hex2rgb returns $expected for $input', ({ input, expected }) => {
  expect(hex2rgb(input)).toEqual(expected);
});
